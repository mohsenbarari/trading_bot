"""Machine-verifiable inventory of externally observable three-site effects.

The inventory distinguishes durable business effects from disposable user-interface
responses.  It is deliberately data rather than prose so a review can detect a new
provider surface that has not received an authority, idempotency, ambiguity, and
readiness decision.
"""

from __future__ import annotations

import hashlib
