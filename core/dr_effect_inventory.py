EFFECT_INVENTORY_SCHEMA = "three-site-dr-effect-inventory-v1"


def _entry(
    name: str,
    *,
    authority: str,
    provider: str,
    durability: str,
    causation: str,
    destination_identity: str,
    idempotency: str,
    claim: str,
    outcomes: list[str],
    ambiguity_policy: str,
    readiness: str,
    source_files: list[str],
    notes: str,
) -> dict[str, Any]:
    return {
        "ambiguity_policy": ambiguity_policy,
        "authority": authority,
        "causation": causation,
        "claim": claim,
        "destination_identity": destination_identity,
        "durability": durability,
        "idempotency": idempotency,
        "name": name,
        "notes": notes,
        "outcomes": outcomes,
        "provider": provider,
        "readiness": readiness,
        "source_files": sorted(source_files),
    }


